// while loop

let idx = 0

while(idx < 10){
    console.log(`Value of log is ${idx}`);
    idx++;
}

let arridx = 0;

let myArr = ['batman', "superman", "flash"]
while(arridx < myArr.length){
    console.log(`The name of hero is ${myArr[arridx]}`);
    arridx++;
}



